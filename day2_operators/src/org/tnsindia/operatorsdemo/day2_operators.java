package org.tnsindia.operatorsdemo;
//relational operator
import java.util.*;
public class day2_operators {
public static void main(String[] args) {
 Scanner s=new Scanner (System.in);
int a=s.nextInt();
int b=s.nextInt();
System.out.println("The addition is: " + (a+b));
System.out.println("The addition is: " + (a-b));
System.out.println("The addition is: " + (a*b));
System.out.println("The addition is: " + (a/b));
System.out.println("The addition is: " + (a%b));
s.close();


	}

}
